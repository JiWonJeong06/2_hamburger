using UnityEngine;

public class SettingsManager : MonoBehaviour
{
     private static SettingsManager instance = null;
    public static SettingsManager Instance
    {
        get
        {
            if (instance == null)
            {
                return null;
            }
            return instance;
        }
    }
    private void Awake()
    {
        if (instance == null)
        {
            instance = this;
            DontDestroyOnLoad(this.gameObject);
        }
        else
        {
            Destroy(this.gameObject);
        }
    }  
}
